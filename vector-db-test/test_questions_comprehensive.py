class ComprehensiveAnswerQualityTester:
    """–¢–µ—Å—Ç–∏—Ä–æ–≤—â–∏–∫ –∫–∞—á–µ—Å—Ç–≤–∞ –æ—Ç–≤–µ—Ç–æ–≤ —Å —Ä–µ–∞–ª—å–Ω—ã–º–∏ API –≤—ã–∑–æ–≤–∞–º–∏"""

    def __init__(self, api_url: str = "http://localhost:8008"):
        self.api_url = api_url
        self.results = []

    async def test_single_question(self, test_case: Dict, document: str) -> Dict:
        """–¢–µ—Å—Ç –æ–¥–Ω–æ–≥–æ –≤–æ–ø—Ä–æ—Å–∞ —Å —Ä–µ–∞–ª—å–Ω—ã–º API –≤—ã–∑–æ–≤–æ–º"""
        result = {
            "document": document,
            "test_id": test_case['id'],
            "question": test_case['question'],
            "passed": False,
            "issues": [],
            "answer": None,
            "sources": None,
            "cost": None
        }

        try:
            async with httpx.AsyncClient(timeout=60.0) as client:
                # –í—ã–∑–æ–≤ API
                response = await client.post(
                    f"{self.api_url}/llm-search",
                    json={
                        "query": test_case['question'],
                        "database": "current",
                        "embedding_model": "paraphrase-multilingual-MiniLM-L12-v2",
                        "top_k": 5,
                        "llm_provider": "yandex",
                        "llm_model": "yandexgpt",
                        "temperature": 0.3
                    }
                )

                if response.status_code == 200:
                    data = response.json()
                    result['answer'] = data.get('llm_response', {}).get('message', {}).get('content', '')
                    result['sources'] = data.get('search_results', [])

                    # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç–∏
                    answer_lower = result['answer'].lower()

                    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–µ —Å–ª–æ–≤–∞
                    for keyword in test_case.get('must_contain', []):
                        if keyword.lower() not in answer_lower:
                            result['issues'].append(f"–û—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç: '{keyword}'")

                    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –¥–ª–∏–Ω—É
                    if len(result['answer']) < test_case.get('min_length', 0):
                        result['issues'].append(f"–ö–æ—Ä–æ—Ç–∫–∏–π –æ—Ç–≤–µ—Ç: {len(result['answer'])} < {test_case['min_length']}")

                    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∏—Å—Ç–æ—á–Ω–∏–∫–∏
                    if not result['sources'] or len(result['sources']) == 0:
                        result['issues'].append("–ù–µ—Ç –∏—Å—Ç–æ—á–Ω–∏–∫–æ–≤")

                    result['passed'] = len(result['issues']) == 0

                else:
                    result['issues'].append(f"API error: {response.status_code}")

        except Exception as e:
            result['issues'].append(f"Exception: {str(e)}")

        return result

